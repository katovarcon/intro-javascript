//Ejercicio 1: Crear un arra con el cuadrado de los números

// Lista de números inicial
let numeros = [2, 4, 6, 8];

// Creamos un nuevo array con el cuadrado de cada número
let cuadrados = numeros.map(num => num * num);

// Mostramos el array de cuadrados
console.log("Array de cuadrados:");
console.log(cuadrados);
