// Declarar variables de tipo let y const
let nombre = "Sofi";
const edad = "29";

//Concatenación de string (cadenas de texto)
console.log("Hola, mi nombre es: " + nombre + " y tengo " + edad + " años. ")
console.log(`Hola, mi nombre es: ${nombre} y tengo ${edad} años.`);

//Cambiar el valor de una variable
nombre = "Laura";
console.log(`Ahora mi nombre es ${nombre} `);

//Cambiar el valor de una constante
edad = "20";
console.log(`Ahora mi edad es ${edad} `);