//Ejercicio 6: Convertir horas a minutos

//Creammos una función declarada
function convertirHorasAMinutos(horas) {
// Multiplicamos las horas por 60 para convertirlas a minutos
    return horas * 60;
  }
  
// Probamos la función en la consola
  console.log(convertirHorasAMinutos(1)); 
  console.log(convertirHorasAMinutos(2)); 
  console.log(convertirHorasAMinutos(0.5)); 
  