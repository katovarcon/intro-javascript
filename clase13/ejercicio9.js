//Ejercicio 9: Contar hasta un número

//Creamos una función declarada
function contarHasta(numero) {
    // Creamos un bucle for que va desde 1 hasta el número dado
    for (let i = 1; i <= numero; i++) {
        // Mostramos cada número en la consola
        console.log(i);
    }
}

// Probamos la función en la consola
contarHasta(5);  