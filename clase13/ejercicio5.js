//Ejercicio 5: Concatenación de nombres

// Creamos una función expresada y la guardamos en una variable
const concatenarNombres = function(nombre, apellido) {
    return nombre + " " + apellido;
  };
  
// Probamos la función en la consola
let nombreCompleto = concatenarNombres("Juan", "Pérez");
console.log(nombreCompleto);
  