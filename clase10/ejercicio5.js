//Ejercicio 5: Operadores

//Declara y asigna un valor numerico a dos variables
let primerNumero = 10;
let segundoNumero = 5;

//Declara las siguientes variables y asígnales el resultado de las operaciones correspondientes
let resultadoSuma = primerNumero + segundoNumero; // Suma
let resultadoResta = primerNumero - segundoNumero; // Resta
let resultadoMultiplicacion = primerNumero * segundoNumero; // Multiplicación
let resultadoDivision = primerNumero / segundoNumero; // División

// Muestra los resultados en la consola
console.log("Resultado de la suma:", resultadoSuma);
console.log("Resultado de la resta:", resultadoResta);
console.log("Resultado de la multiplicación:", resultadoMultiplicacion);
console.log("Resultado de la división:", resultadoDivision);
